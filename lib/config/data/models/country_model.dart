class CountryModel {
  final String englishName;
  final String arabicName;
  final String phoneCode;
  final String flag;
  final String langCode;
  final int phoneNumberLengthWithoutCode;

  CountryModel(
      {required this.englishName,
      required this.arabicName,
      required this.phoneCode,
      required this.phoneNumberLengthWithoutCode,
      required this.flag,
      required this.langCode});

  String getTranslatedCountryName(BuildContext context) {
    final lang = LocalizationHelper.getCurrentLanguageByContext(context);
    return lang == AppLanguage.ar ? arabicName : englishName;
  }

  String getCountryCodeOrderd(BuildContext context) {
    final textDirection = Directionality.of(context);
    if (textDirection == TextDirection.rtl) {
      return "${phoneCode.substring(1)}+";
    } else {
     return phoneCode;
    }
  }
  int get totalPhoneNumberLength => phoneCode.length + phoneNumberLengthWithoutCode;
}

